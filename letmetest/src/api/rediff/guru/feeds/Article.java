package api.rediff.guru.feeds;

public class Article {
	private String abstract_report;
	private String mobile_headline;
	private String image_url;
	private String live_url;
	private String tags;
	private String topics;
	private String byline_screen;
	private String byline_image;
	private String byline_display;
	private String utime;

	public String getAbstract_report() {
		return abstract_report;
	}

	public void setAbstract_report(String abstract_report) {
		this.abstract_report = abstract_report;
	}

	public String getMobile_headline() {
		return mobile_headline;
	}

	public void setMobile_headline(String mobile_headline) {
		this.mobile_headline = mobile_headline;
	}

	public String getImage_url() {
		return image_url;
	}

	public void setImage_url(String image_url) {
		this.image_url = image_url;
	}

	public String getLive_url() {
		return live_url;
	}

	public void setLive_url(String live_url) {
		this.live_url = live_url;
	}

	public String getTags() {
		return tags;
	}

	public void setTags(String tags) {
		this.tags = tags;
	}

	public String getTopics() {
		return topics;
	}

	public void setTopics(String topics) {
		this.topics = topics;
	}

	public String getByline_screen() {
		return byline_screen;
	}

	public void setByline_screen(String byline_screen) {
		this.byline_screen = byline_screen;
	}

	public String getByline_image() {
		return byline_image;
	}

	public void setByline_image(String byline_image) {
		this.byline_image = byline_image;
	}

	public String getByline_display() {
		return byline_display;
	}

	public void setByline_display(String byline_display) {
		this.byline_display = byline_display;
	}

	public String getUtime() {
		return utime;
	}

	public void setUtime(String utime) {
		this.utime = utime;
	}

}
